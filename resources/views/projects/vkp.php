                <img src="/img/projects/vkp/pictures.png">
            </div>
            <p>
                There are 4 different tests that can be taken as well as the automated test which takes you through
                a randomly selected variety of questions, becoming gradually more difficult as you get questions wrong
                and re-testing areas where the results are questionable.
            </p>
            <div class="image">
                <img src="/img/projects/vkp/vocab.png">
