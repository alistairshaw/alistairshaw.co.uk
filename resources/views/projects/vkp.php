                items. The words tested are sampled from the most frequent 10,000 content words (lemmas) in the Corpus
                of Contemporary American English (COCA). It reports your scores according to your mastery of different
                frequency levels.
            </p>
            <div class="image">
                <img src="/img/projects/vkp/test-complete.png">
            </div>
